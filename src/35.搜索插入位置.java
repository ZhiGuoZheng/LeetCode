class Solution {
    public int searchInsert(int[] nums, int target) {
        // 使用二分查找法
        // 定义左边界
        int low = 0;
        // 定义右边界
        int high = nums.length - 1;
        // 中间指针
        int mid = 0;
        // 不越界
        while(low <= high) {
            // 计算中间索引
            mid = (low + high) / 2;
            // 相等时
            if (target == nums[mid]) {
                return mid;
            } else if (target > nums[mid]) {
                low = mid + 1;
            } else {
                high = mid - 1;
            }
        }
        return low;
    }
}

